    function triggerGlobalSOS() {
        if (!confirm('CRITICAL ALERT: This will dispatch an emergency SOS signal to Authorities. Are you in immediate danger?')) return;

        const btn = document.getElementById('globalSosBtn');
        if (btn) {
            btn.style.animation = 'none';
            btn.innerHTML = '<span class="material-symbols-outlined" style="animation: spin 1s linear infinite;">sync</span>';
        }

        if (navigator.geolocation) {
            navigator.geolocation.getCurrentPosition(
                pos => sendSOS(pos.coords.latitude, pos.coords.longitude),
                ()  => sendSOS(null, null)
            );
        } else {
            sendSOS(null, null);
        }
    }

    async function sendSOS(lat, lng) {
        const result = await API.post('api/sos_trigger.php', { 
            latitude: lat, 
            longitude: lng 
        });
        
        if (result.success && result.data.success) {
            alert('SOS BEACON ACTIVATED. Responders have been notified.');
            window.location.reload();
        } else {
            alert('SOS Transmission Failed: ' + (result.data?.message || result.error || 'Unknown Error'));
            const btn = document.getElementById('globalSosBtn');
            if (btn) btn.innerHTML = '<span class="material-symbols-outlined">sos</span>';
            console.error('SOS Error:', result);
        }
    }
